#include <unistd.h>

void	ft(char c)
{
	write(1, &c, 1);
}

int	main(int ac, char **av)
{
	int	i;
	int	j;

	i = ac - 1;
	while (i > 0)
	{
		j = 0;
		while (av[i][j])
		{
			ft(av[i][j]);
			j++;
		}
		ft('\n');
		i--;
	}
}
